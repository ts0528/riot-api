                <p id="panth">
                    <h2>panthとは</h2></p>
                ほぼ最初期からtopに君臨する槍投げマン時々MIDに出没したりする
                <br>topで槍投げ選手権が始まるとほぼ一方的に相手は槍で貫かれ死ぬ
                <br>最近では槍投げではなく槍の刺突で敵を屠ることもあるらしい
                <br>仕上げにtopにいたと思ったら唐突にbotやmidに降ってきたりするらしい　何なんだこいつ
                <br>そんな理不尽極まりないチャンプなので使いこなせばfavariteなチャンプになるでしょう

                <br>何が強いのかというと上で上げたqのスキルが万能な点です
                <br>4秒毎にレンジ600もあるスキルがバンバン飛んで来るのは相手からしたら悪夢以外の何物でもないでしょう
                <br>仕掛けたら仕掛けたらでPassiveでのaa無効化（一回だけ）wでのstunそこから始めるeでのダメージと離れた距離からのq　基本的にはダメージトレードが成り立ちません
                <br>弱い点はultがレーン単体で見ると腐ることです
                <br>相手が究極技をレーンで使えるのにこっちはまず使えません
                <br>またgankに対する耐性も持ち合わせていないのでまず死にます
                <br>そして後半になるにつれてultがないということで1v1ですら負けるようになります

